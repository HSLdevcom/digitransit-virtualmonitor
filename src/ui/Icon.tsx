export const iconExists = key => {
  const keys = Object.keys(IconMap(null));
  if (keys.includes(key)) {
    return true;
  }
  return false;
};

/**
 * Icon renders predefined Svg icons as react component.
 * @example
 * <Icon
 *    img="bus"       // Key of svg, required
 *    height={24}      // Height as px, optional
 *    width={24}       // Width as px, optional
 *    color="#007ac9" // Color of image, optional
 *    rotate={90}     // How many degrees to rotate image, optional
 * />
 */
const Icon = (props: ICustomInputProps) => {
  const background = null;
  let img = props.img;
  let fill = props.color;
  let height = props.height ? `${props.height}px` : '24px';
  let width = props.width ? `${props.width}px` : '24px';
  let stroke = null;
  if (img === 'check') {
    fill = null;
    height = '18';
    width = '14';
    stroke = props.color;
  }
  const style = {
    fill: fill,
    stroke: stroke,
    height: height,
    width: width,
    transform: props.rotate ? `rotate(${props.rotate}deg)` : null,
    background: background,
    borderRadius: props.borderRadius ? props.borderRadius : null,
    margin: props.margin ? `${props.margin}` : '0',
  };

  if (img && img.indexOf('checkbox-') !== -1) {
    const isFocus = img.indexOf('-focus') !== -1;
    style['outlineStyle'] = isFocus ? 'auto' : 'none';
    style['outlineOffset'] = isFocus ? '2px' : 'none';
    img = isFocus ? img.substring(0, img.indexOf('-focus')) : img;
  }

  const icons = IconMap(style);
  return <>{icons[img]}</>;
};

export default Icon;
